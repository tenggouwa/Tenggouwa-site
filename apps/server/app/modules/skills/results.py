"""工具结果的状态前缀（一眼可辨的约定）。

模型（尤其 DeepSeek）容易把「查了没有」和「工具坏了」混为一谈——前者可换个实质不同的角度再试
或直接收口，后者重试通常白费。给这两类结果加个自解释前缀，让模型不必读整句中文去猜下一步。

- empty：工具正常跑了、但没命中 → 别反复换措辞搜同一件事，换角度或就说没有。
- error：工具本身失败（网络 / 依赖 / 超时）→ 重试通常无用，别死磕，换条路或如实说明。

成功结果不加前缀（最常见，加了只是噪声）；参数错 / 通道不可用那类「改你的调用」的提示也不用它。
"""


def empty(msg: str) -> str:
    return f"[无结果] {msg}"


def error(msg: str) -> str:
    return f"[出错] {msg}"
